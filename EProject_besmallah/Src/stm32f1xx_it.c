/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

   if(min_ticks==60000 ){
		  
		 if(tr_flag==0x0){
			 char s[]= "BPM value is\n";
			 HAL_UART_Transmit(&huart1, (uint8_t *)s, strlen(s), HAL_MAX_DELAY);
			 sprintf(Bpm, "%d\r\n", sum_cnt);
		   HAL_UART_Transmit(&huart1, (uint8_t *)Bpm, strlen(Bpm), HAL_MAX_DELAY);
			 tr_flag =0x1;
		 }
		  flag=0;
	    HAL_ADC_Stop_IT(&hadc1);
		}
		else if (flag == 1){
			min_ticks = min_ticks+1;
			if(sample_ticks==gen_rate-1){
				//adc_val_prev = adc_val;
				sprintf(msg, "%d\r\n", adc_val);
			  HAL_UART_Transmit(&huart1, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
				sample_ticks = 0;
				if(adc_val> 0xBB8){
					bpmFlag=1;
				}
				if( (adc_val< 0x1F4) && (bpmFlag ==1) ){
					bpmFlag=0;
					sum_cnt = sum_cnt+1;
				}
			}
      else {
				sample_ticks = sample_ticks+1;
			}				
		}
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  /* USER CODE END SysTick_IRQn 1 */
}

